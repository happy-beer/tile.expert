CREATE TABLE IF NOT EXISTS orders (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hash VARCHAR(32) NOT NULL,
    user_id INTEGER NULL,
    token VARCHAR(64) NOT NULL,
    number VARCHAR(10) NULL,
    status INTEGER NOT NULL DEFAULT 1,
    email VARCHAR(100) NULL,
    vat_type INTEGER NOT NULL DEFAULT 0,
    vat_number VARCHAR(100) NULL,
    tax_number VARCHAR(50) NULL,
    discount SMALLINT NULL,
    delivery DOUBLE PRECISION NULL,
    delivery_type SMALLINT NULL DEFAULT 0,
    delivery_time_min DATE NULL,
    delivery_time_max DATE NULL,
    delivery_time_confirm_min DATE NULL,
    delivery_time_confirm_max DATE NULL,
    delivery_time_fast_pay_min DATE NULL,
    delivery_time_fast_pay_max DATE NULL,
    delivery_old_time_min DATE NULL,
    delivery_old_time_max DATE NULL,
    delivery_index VARCHAR(20) NULL,
    delivery_country INTEGER NULL,
    delivery_region VARCHAR(50) NULL,
    delivery_city VARCHAR(200) NULL,
    delivery_address VARCHAR(300) NULL,
    delivery_building VARCHAR(200) NULL,
    delivery_phone_code VARCHAR(20) NULL,
    delivery_phone VARCHAR(20) NULL,
    sex SMALLINT NULL,
    client_name VARCHAR(255) NULL,
    client_surname VARCHAR(255) NULL,
    company_name VARCHAR(255) NULL,
    pay_type SMALLINT NOT NULL,
    pay_date_execution TIMESTAMP NULL,
    offset_date TIMESTAMP NULL,
    offset_reason SMALLINT NULL,
    proposed_date TIMESTAMP NULL,
    ship_date TIMESTAMP NULL,
    tracking_number VARCHAR(50) NULL,
    manager_name VARCHAR(20) NULL,
    manager_email VARCHAR(30) NULL,
    manager_phone VARCHAR(20) NULL,
    carrier_name VARCHAR(50) NULL,
    carrier_contact_data VARCHAR(255) NULL,
    locale VARCHAR(5) NOT NULL,
    cur_rate DOUBLE PRECISION NULL DEFAULT 1,
    currency VARCHAR(3) NOT NULL DEFAULT 'EUR',
    measure VARCHAR(3) NOT NULL DEFAULT 'm',
    name VARCHAR(200) NOT NULL,
    description VARCHAR(1000) NULL,
    create_date TIMESTAMP NOT NULL,
    update_date TIMESTAMP NULL,
    warehouse_data TEXT NULL,
    step SMALLINT NOT NULL DEFAULT 1,
    address_equal BOOLEAN NULL DEFAULT TRUE,
    bank_transfer_requested BOOLEAN NULL,
    accept_pay BOOLEAN NULL,
    cancel_date TIMESTAMP NULL,
    weight_gross DOUBLE PRECISION NULL,
    product_review BOOLEAN NULL,
    mirror SMALLINT NULL,
    process BOOLEAN NULL,
    fact_date TIMESTAMP NULL,
    entrance_review SMALLINT NULL,
    payment_euro BOOLEAN NULL DEFAULT FALSE,
    spec_price BOOLEAN NULL,
    show_msg BOOLEAN NULL,
    delivery_price_euro DOUBLE PRECISION NULL,
    address_payer INTEGER NULL,
    sending_date TIMESTAMP NULL,
    delivery_calculate_type SMALLINT NULL DEFAULT 0,
    full_payment_date DATE NULL,
    bank_details TEXT NULL,
    delivery_apartment_office VARCHAR(30) NULL
);

CREATE INDEX IF NOT EXISTS idx_orders_delivery_country ON orders (delivery_country);
CREATE INDEX IF NOT EXISTS idx_orders_user_id ON orders (user_id);
CREATE INDEX IF NOT EXISTS idx_orders_create_date ON orders (create_date);
CREATE INDEX IF NOT EXISTS idx_orders_create_date_status ON orders (create_date, status);
CREATE INDEX IF NOT EXISTS idx_orders_hash ON orders (hash);

CREATE TABLE IF NOT EXISTS orders_article (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    orders_id INTEGER NULL,
    article_id INTEGER NULL,
    amount DOUBLE PRECISION NOT NULL,
    price DOUBLE PRECISION NOT NULL,
    price_eur DOUBLE PRECISION NULL,
    currency VARCHAR(3) NULL,
    measure VARCHAR(2) NULL,
    delivery_time_min DATE NULL,
    delivery_time_max DATE NULL,
    weight DOUBLE PRECISION NOT NULL,
    multiple_pallet SMALLINT NULL,
    packaging_count DOUBLE PRECISION NOT NULL,
    pallet DOUBLE PRECISION NOT NULL,
    packaging DOUBLE PRECISION NOT NULL,
    swimming_pool BOOLEAN NOT NULL DEFAULT FALSE,
    CONSTRAINT fk_orders_article_orders
        FOREIGN KEY (orders_id)
        REFERENCES orders (id)
        ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_orders_article_article_id ON orders_article (article_id);
CREATE INDEX IF NOT EXISTS idx_orders_article_orders_id ON orders_article (orders_id);
